
package modelo;

/**
 *
 * @author Raul
 */
public class Usuario {
    
    private String nombre;
    private int edad;
    private String pass;

    public Usuario() {
    }

    public String getNombre() {
        return nombre;
    }

    public int getEdad() {
        return edad;
    }

    public String getPass() {
        return pass;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }
    
    
    
}
